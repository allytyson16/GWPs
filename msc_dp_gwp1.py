def european_trinomial_option(S0, K, r, sigma, T, N, option_type="call"):
    """
    Prices a European option using a recombining trinomial tree.
    option_type can be 'call' or 'put'.
    """

    dt = T / N

    # Trinomial tree parameters
    u = np.exp(sigma * np.sqrt(2 * dt))
    d = 1 / u

    # Risk-neutral probabilities
    pu = (
        (np.exp(r * dt / 2) - np.exp(-sigma * np.sqrt(dt / 2)))
        / (np.exp(sigma * np.sqrt(dt / 2)) - np.exp(-sigma * np.sqrt(dt / 2)))
    ) ** 2

    pd = (
        (np.exp(sigma * np.sqrt(dt / 2)) - np.exp(r * dt / 2))
        / (np.exp(sigma * np.sqrt(dt / 2)) - np.exp(-sigma * np.sqrt(dt / 2)))
    ) ** 2

    pm = 1 - pu - pd

    # Continuous discount factor per step
    discount = np.exp(-r * dt)

    # Stock prices at maturity
    final_nodes = np.arange(-N, N + 1)
    stock_prices = S0 * (u ** final_nodes)

    # Option payoff at maturity
    if option_type == "call":
        option_values = np.maximum(stock_prices - K, 0)
    elif option_type == "put":
        option_values = np.maximum(K - stock_prices, 0)
    else:
        raise ValueError("option_type must be 'call' or 'put'")

    # Backward induction
    for i in range(N - 1, -1, -1):
        option_values = discount * (
            pd * option_values[0 : 2 * i + 1]
            + pm * option_values[1 : 2 * i + 2]
            + pu * option_values[2 : 2 * i + 3]
        )

    return option_values[0]


