def simulate_binomial_paths(S0, r, sigma, T, N, M, seed=123):
    """
    Simulate stock price paths using a CRR binomial model.
    """

    rng = np.random.default_rng(seed)

    dt = T / N

    u = math.exp(sigma * math.sqrt(dt))
    d = 1 / u

    p = (math.exp(r * dt) - d) / (u - d)

    random_values = rng.random((M, N))
    up_moves = random_values < p

