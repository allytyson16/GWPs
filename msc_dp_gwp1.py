def binomial_american(S0, K, r, sigma, T, n, option_type='call'):
    """
    Price American call or put using a binomial tree.
    """
    dt = T / n
    u = np.exp(sigma * np.sqrt(dt))
    d = 1 / u
    p = (np.exp(r * dt) - d) / (u - d)
    discount = np.exp(-r * dt)

    # Stock prices at maturity
    S_T = np.array([S0 * (u ** (n - j)) * (d ** j) for j in range(n+1)])

    # Payoffs at maturity
    if option_type == 'call':
        V = np.maximum(S_T - K, 0)
    else:
        V = np.maximum(K - S_T, 0)

    # Backward induction with early exercise check
    for i in range(n-1, -1, -1):
        # Continuation value
        V_cont = discount * (p * V[:-1] + (1-p) * V[1:])
        # Stock price at current node (for i steps remaining)
        S_curr = np.array([S0 * (u ** (i - j)) * (d ** j) for j in range(i+1)])
        if option_type == 'call':
            V = np.maximum(V_cont, S_curr - K)
        else:
            V = np.maximum(V_cont, K - S_curr)
    return V[0]

