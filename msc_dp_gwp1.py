    """Delta at node (i, j): j = number of down moves"""
    if i == N:
        return 0.0
    S_up = S_tree[i+1][j]      # same j (no additional down)
    S_down = S_tree[i+1][j+1]  # one more down
    V_up = V_tree[i+1][j]
    V_down = V_tree[i+1][j+1]
    return (V_up - V_down) / (S_up - S_down)

