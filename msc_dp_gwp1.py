def american_put_binomial_full(S0, K, r, sigma, T, N):
    dt = T / N
    u = np.exp(sigma * np.sqrt(dt))
    d = 1 / u
    p = (np.exp(r * dt) - d) / (u - d)
    disc = np.exp(-r * dt)

    # Build stock price tree (list of arrays)
    S_tree = []
    for i in range(N + 1):
        row = np.array([S0 * (u ** (i - j)) * (d ** j) for j in range(i + 1)])
        S_tree.append(row)

    # Option values at maturity
    V = np.maximum(K - S_tree[N], 0)
    V_tree = [None] * (N + 1)
    V_tree[N] = V

    # Backward induction (American)
    for i in range(N - 1, -1, -1):
        cont = disc * (p * V[:-1] + (1 - p) * V[1:])
        V = np.maximum(cont, K - S_tree[i])
        V_tree[i] = V

    put_price = V_tree[0][0]
    return put_price, S_tree, V_tree

